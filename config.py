DATA_PATH = ""
DATA_TTREE = "Events"
METADATA_TTREE = "LuminosityBlocks"

def set_data_path(path: str):
    global DATA_PATH
    DATA_PATH = path

def set_data_ttree(ttree: str):
    global DATA_TTREE
    DATA_TTREE = ttree 

def set_metadata_ttree(ttree: str):
    global METADATA_TTREE
    METADATA_TTREE = ttree 